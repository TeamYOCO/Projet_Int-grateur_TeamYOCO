public class PathAnimation {
    
    private int startX;
    private int startY;
    private int endX;
    private int endY;
    private int speed;

    public PathAnimation(int startX, int startY, int endX, int endY, int speed) {
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
        this.speed = speed;
    }
    
    
}
